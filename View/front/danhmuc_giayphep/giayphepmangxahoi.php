            </h4>
            <p>
                Doanh nghiệp, tổ chức chuẩn bị 02 bộ hồ sơ cấp giấy phép đáp ứng đủ các điều kiện xin giấy phép mạng xã
                hội (01 bộ nộp Bộ Thông tin và Truyền thông, 01 bộ lưu tại doanh nghiệp, tổ chức xin cấp phép).
            </p>
